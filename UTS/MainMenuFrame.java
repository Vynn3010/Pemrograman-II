private void refreshList() {
    jPanelList.removeAll();

    for (AkunGame akun : getFilteredList()) {
        jPanelList.add(createCardPanel(akun));
        jPanelList.add(Box.createVerticalStrut(12)); // jarak antar card
    }
    jPanelList.add(Box.createVerticalGlue());

    jPanelList.revalidate();
    jPanelList.repaint();
}
private JPanel createCardPanel(AkunGame akun) {
    JPanel card = new JPanel();
    card.setLayout(new BoxLayout(card, BoxLayout.Y_AXIS));
    card.setBackground(new Color(45, 52, 64));
    card.setBorder(BorderFactory.createCompoundBorder(
            BorderFactory.createLineBorder(new Color(120, 120, 120)),
            BorderFactory.createEmptyBorder(12, 12, 12, 12)
    ));
    card.setAlignmentX(Component.LEFT_ALIGNMENT);

    JLabel lblJudul = new JLabel(akun.getJudul());
    lblJudul.setForeground(Color.WHITE);
    lblJudul.setFont(lblJudul.getFont().deriveFont(Font.BOLD, 16f));
    lblJudul.setAlignmentX(Component.LEFT_ALIGNMENT);

    JLabel lblInfo = new JLabel("Login via: " + akun.getLoginVia() + "   |   Status: " + akun.getStatus());
    lblInfo.setForeground(new Color(220, 220, 220));
    lblInfo.setAlignmentX(Component.LEFT_ALIGNMENT);

    JLabel lblHarga = new JLabel("Harga: Rp " + akun.getHarga());
    lblHarga.setForeground(Color.WHITE);
    lblHarga.setFont(lblHarga.getFont().deriveFont(Font.BOLD, 14f));
    lblHarga.setAlignmentX(Component.LEFT_ALIGNMENT);

    JLabel lblCatatan = new JLabel("<html><span style='color:#DDDDDD'>Catatan:</span> "
            + escapeHtml(akun.getCatatan()) + "</html>");
    lblCatatan.setForeground(new Color(220, 220, 220));
    lblCatatan.setAlignmentX(Component.LEFT_ALIGNMENT);

    JButton btnBeli = new JButton("Beli");
    btnBeli.setAlignmentX(Component.LEFT_ALIGNMENT);
    
    
    if ("Sold Out".equalsIgnoreCase(akun.getStatus())) {
        btnBeli.setEnabled(false);
        btnBeli.setText("Sold Out");
    }

    card.add(lblJudul);
    card.add(Box.createVerticalStrut(6));
    card.add(lblInfo);
    card.add(Box.createVerticalStrut(6));
    card.add(lblHarga);
    card.add(Box.createVerticalStrut(6));
    card.add(lblCatatan);
    card.add(Box.createVerticalStrut(10));
    card.add(btnBeli);

    return card;
}
private String escapeHtml(String s) {
    if (s == null) return "";
    return s.replace("&", "&amp;")
            .replace("<", "&lt;")
            .replace(">", "&gt;")
            .replace("\"", "&quot;");
}


