    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
            String judul = txtJudul.getText().trim();
    String email = txtEmail.getText().trim();
    String password = new String(txtPassword.getPassword()).trim();
    String loginVia = cbLoginVia.getSelectedItem().toString();
    String catatan = txtCatatan.getText().trim();
    String status = cbStatus.getSelectedItem().toString();

    int harga;
    try {
        harga = Integer.parseInt(txtHarga.getText().trim());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Harga harus angka!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    if (judul.isEmpty() || loginVia.isEmpty() || txtHarga.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Judul dan Harga wajib diisi!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    AkunGame akun = new AkunGame(judul, email, password, loginVia, catatan, status, harga);
    SystemManager.stok.add(akun);

    refreshList();
    clearForm();
    }//GEN-LAST:event_btnSimpanActionPerformed

