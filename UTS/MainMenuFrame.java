private java.util.List<AkunGame> getFilteredList() {
    String q = txtSearch.getText().trim().toLowerCase();
    String filter = cbFilter.getSelectedItem().toString();
    String sort = cbSort.getSelectedItem().toString();

    java.util.List<AkunGame> result = new java.util.ArrayList<>();

    // FILTER + SEARCH
    for (AkunGame akun : SystemManager.stok) {

        // filter status
        if (!"Semua".equalsIgnoreCase(filter)) {
            if (!akun.getStatus().equalsIgnoreCase(filter)) continue;
        }

        // search judul
        if (!q.isEmpty()) {
            String judul = akun.getJudul().toLowerCase();
            if (!judul.contains(q)) continue;
        }

        result.add(akun);
    }

    // ===== SORT =====
    if ("Terbaru".equalsIgnoreCase(sort)) {
        // biarin urutan default (yang terakhir ditambah di bawah)
        // supaya terbaru di atas â†’ dibalik
        java.util.Collections.reverse(result);

    } else if ("Terlama".equalsIgnoreCase(sort)) {
        // urutan normal (tidak usah diapa-apain)

    } else if ("Harga".equalsIgnoreCase(sort)) {
        result.sort((a, b) -> Integer.compare(a.getHarga(), b.getHarga()));
    }

    return result;
}
