    private void btnBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBayarActionPerformed
        // TODO add your handling code here:
        
        // cek nominal harus angka dan harus sama dengan harga akun
String nominalStr = txtNominal.getText().trim();
int nominal;

try {
    nominal = Integer.parseInt(nominalStr);
} catch (NumberFormatException ex) {
    JOptionPane.showMessageDialog(this, "Nominal harus angka!", "Validasi", JOptionPane.WARNING_MESSAGE);
    return;
}

if (nominal != akun.getHarga()) {
    JOptionPane.showMessageDialog(
        this,
        "Nominal tidak sesuai!\nHarus: Rp " + akun.getHarga(),
        "Pembayaran Gagal",
        JOptionPane.WARNING_MESSAGE
    );
    return;
}

    // 1) Cegah beli ulang kalau sudah sold out
    if ("Sold Out".equalsIgnoreCase(akun.getStatus())) {
        JOptionPane.showMessageDialog(this, "Akun sudah Sold Out!", "Gagal", JOptionPane.WARNING_MESSAGE);
        dispose();
        return;
    }

    // 2) Ambil metode pembayaran dari combobox
    String metode = jComboBox1.getSelectedItem().toString();

    // 3) (Opsional) cek PIN kosong
    String pin = new String(txtPin.getPassword()).trim();
    if (pin.isEmpty()) {
        JOptionPane.showMessageDialog(this, "PIN tidak boleh kosong!", "Validasi", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // 4) Konfirmasi simulasi payment
    int ok = JOptionPane.showConfirmDialog(
            this,
            "Metode: " + metode + "\nLanjut bayar?",
            "Konfirmasi Pembayaran",
            JOptionPane.OK_CANCEL_OPTION
    );

    if (ok != JOptionPane.OK_OPTION) return;

    // 5) Update akun: Sold Out + unlock
    akun.setStatus("Sold Out");
    akun.setBuyerUnlocked(true);

    JOptionPane.showMessageDialog(this, "Pembayaran berhasil âœ…", "Sukses", JOptionPane.INFORMATION_MESSAGE);

    // 6) Refresh MainMenuTamu supaya card berubah & email/password muncul
    if (parent != null) {
        parent.refreshAfterPayment();
    }

    // 7) Tutup PaymentFrame
    dispose();

    }//GEN-LAST:event_btnBayarActionPerformed

